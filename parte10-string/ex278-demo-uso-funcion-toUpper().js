// Ejercicio 278: Usar la función toUpper() para dejar en mayúscula el contenido de un objeto String.

const _ = require('lodash');

console.log(_.toUpper('¡JavaScript es tremendo!')); // ¡JAVASCRIPT ES TREMENDO!
console.log(_.toUpper('¡javascript es tremendo!')); // ¡JAVASCRIPT ES TREMENDO!
console.log(_.toUpper('***¡javascript es tremendo!***')); // ***¡JAVASCRIPT ES TREMENDO!***
