// Ejercicio 277: Usar la función toLower() para dejar en minúscula el contenido de un objeto String.

const _ = require('lodash');

console.log(_.toLower('JavaScript Es Genial')); // javascript es genial
console.log(_.toLower('JavaScript-Es-Genial')); // javascript-es-genial
console.log(_.toLower('JAVASCRIPT ES GENIAL')); // javascript es genial
