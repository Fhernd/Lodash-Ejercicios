// Ejercicio 285: Usar la función upperFirst() para dejar en mayúscula la primera letra de una cadena.

const _ = require('lodash');

console.log(_.upperFirst('lodash'));    // Lodash
console.log(_.upperFirst('lodash es tremendo'));    // Lodash es tremendo
console.log(_.upperFirst('1 lodash es tremendo'));    // 1 lodash es tremendo
